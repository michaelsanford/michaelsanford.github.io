function setLanguage(lang) {
  const strings = translations[lang];
  if (!strings) return;

  document.documentElement.lang = lang;

  // Update standard i18n text content or img alt
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!strings[key]) return;

    if (el.tagName === "IMG") {
      el.alt = strings[key];
    } else {
      el.textContent = strings[key];
    }
  });

  // Update aria-labels if specified
  document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria-label");
    if (strings[key]) {
      el.setAttribute("aria-label", strings[key]);
    }
  });

  // Update titles if specified
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    if (strings[key]) {
      el.setAttribute("title", strings[key]);
    }
  });

  // Update meta descriptions
  if (strings.meta_desc) {
    document.querySelector('meta[name="description"]')?.setAttribute('content', strings.meta_desc);
    document.querySelector('meta[property="og:description"]')?.setAttribute('content', strings.meta_desc);
    document.querySelector('meta[name="twitter:description"]')?.setAttribute('content', strings.meta_desc);
  }

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const isActive = btn.dataset.lang === lang;
    btn.classList.toggle("active", isActive);
    btn.setAttribute("aria-pressed", isActive ? "true" : "false");
  });

  localStorage.setItem("lang", lang);
}

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
});

setLanguage(localStorage.getItem("lang") || "en");
